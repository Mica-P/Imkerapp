            <p>Hommels worden nog wel eens verward met wespen en bijen. Mensen zijn daarom nog weleens bang dat ze aangevallen
                worden door de hommel, om die reden willen ze dan ook geen hommelnest in de tuin. Ook rondvliegende hommels
                worden nog weleens gedood, uit angst voor steken. Een hommelnest verwijderen is eigenlijk niet nodig.
                Een hommel is niet agressief en het nest bestaat uit maximaal 150 exemplaren. Hommels zijn altijd druk bezig
                met het verzamelen van stuifmeel en steken bijna niet. Alleen als er direct gevaar dreigt voor een hommel kan
                zij steken. Een hommelnest kan je het beste lekker laten zitten, tenzij het nest in de weg zit zoals bij een
                voordeur van een huis. Als er veel menselijke activiteit is in de vliegroute van de hommels op en neer naar
                hun nest dan kan het verstandig zijn het hommelnest te laten verplaatsen door een imker. Zit uw hommelnest
                in de weg dan kunt u contact opnemen met de Imkers van Imkervereniging Oegstgeest.</p>
